package lessons;

public class DecisionStructures {

	public static void main(String[] args) {

		// Comparison Operators: >, >=, <, <=, ==, !=
		// When we compare numbers, the result is a boolean value (true/false).
		// Boolean values can be used for conditional statements (if/else).
		
		if (13 != 7)  {
			
			System.out.println("True");
		}
		
		else  {
			
			System.out.println("False");
		}

	}

}
